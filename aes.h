void generate_round_keys(struct block key,struct block round_key[11])
{	struct block temp;	
	int i;	
	round_key[0]=key;
	temp=key;
	for(i=0;i<10;i++)
	{	temp=next_key(temp,i);
		round_key[i+1]=temp;
	}
}
