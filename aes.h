struct block prepare_key(unsigned char s[16])
{	struct block key;	
	int i,j;	// Counter for key
	int k=0;	// Counter for string.
	for(i=0;i<4;i++)
	{	for(j=0;j<4;j++)
		{	key.b[i][j]=s[k];
			k++;
		}
	}
	return key;
}

