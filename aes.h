void swap(unsigned char *a,unsigned char *b)
{	unsigned char temp;
	temp=*a;
	*a=*b;
	*b=temp;
}

void left_shift_row(char *a)
{	int i;	
	char c=*a;
	for(i=0;i<3;i++)
	{	swap(a+i,a+i+1);
	}
}

void right_shift_row(char *a)
{	int i;
	//char c=a[3];
	for(i=3;i>0;i--)
	{	swap(a+i,a+i-1);
	}
}

